import random

def jogar():

    bem_vindo()

    palavra_secreta = carregar_palavra()

    letras_acertadas = inicializa_letras_acertadas(palavra_secreta)
    print(letras_acertadas)

    enforcou = False
    acertou = False
    erros = 0


    #Enquanto não enforcou & não acertou.
    while(not enforcou and not acertou):

        chute = pede_chute()

        if(chute in palavra_secreta):
            marca_chute_correto(chute, letras_acertadas, palavra_secreta)
        else:
            erros += 1
            desenha_forca(erros)
            print(f"Você errou, faltam {6 - erros} tentativas.")

        enforcou = erros == 6
        acertou = "_" not in letras_acertadas
        print(letras_acertadas)

    if(acertou):
        imprime_mensagem_vencedor()
    else:
        imprime_mensagem_perdeu(palavra_secreta)

    print("Fim do jogo!")


def bem_vindo():
    print("*********************************")
    print("***Bem vindo no jogo da Forca ***!")
    print("*********************************", end="\n\n")

def carregar_palavra():
    arquivo = open("palavras.txt", "r")  # Apenas read no arquivo txt. "w" seria para escrever um mesmo.
    palavras = []

    for linha in arquivo:
        linha = linha.strip()
        palavras.append(linha)

    arquivo.close()  # Boa prática sempre fechar o arquivo.

    numero = random.randrange(0, len(palavras))
    return palavras[numero].upper()

def inicializa_letras_acertadas(palavra_secreta):
    return ["_" for letra in palavra_secreta]

def pede_chute():
    chute =input("Qual a letra?")
    chute = chute.strip().upper()
    return chute

def marca_chute_correto(chute, letras_acertadas, palavra_secreta):
    index = 0
    for letra in palavra_secreta:
        if (chute == letra):
            letras_acertadas[index] = letra
        index += 1

